#? stdlib
`format.checkRdContents` <- function (x, ...) 
{
    glimpse <- function(x, tail = FALSE) {
        x <- lines2str(x, " ")
        if ((nc <- nchar(x)) > 63L) {
            if (tail) 
                paste0("...", substr(x, nc - 59L, nc))
            else paste0(substr(x, 0, 60L), "...")
        }
        else x
    }
    .fmt <- function(nm) {
        y <- x[[nm]]
        c(character(), if (length(arguments_with_no_description <- y[["arguments_with_no_description"]])) {
            c(gettextf("Argument items with no description in Rd file '%s':", 
                nm), .pretty_format(arguments_with_no_description))
        }, if (length(autocontents <- y[["offending_autogenerated_content"]])) {
            c(gettextf("Auto-generated content requiring editing in Rd file '%s':", 
                nm), sprintf("  %s: %s", autocontents[, 1L], 
                sQuote(mapply(glimpse, autocontents[, 2L], autocontents[, 
                  1L] == "\\details"))))
        })
    }
    c(character(), if (length(res <- which(vapply(x, `[[`, TRUE, 
        "missing_description")))) c(gettext("Rd files without \\description:"), 
        .pretty_format(names(res))), as.character(unlist(lapply(names(x), 
        .fmt))))
}
